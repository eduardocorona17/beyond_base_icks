import datetime
# spam is a variable containing a datetime object
spam = datetime.datetime.now()
# year and month are attributes of that object.
print(spam.year)
print(spam)
print(spam.month)

# len() is a function
print(len('Hello'))

# upper() is a string method
print('Hello'.upper())

import math
# sqrt() function is associated with 'math' which is a module, not a class
print(math.sqrt(25))